/**
 * 获取好感度级别对应的情话
 * @param {number} happiness - 好感度
 * @returns {array} 情话数组
 */
export function getMessagesByHappiness(happiness) {
  if (happiness >= 100) return MESSAGES.level100;
  if (happiness >= 90) return MESSAGES.level90;
  if (happiness >= 80) return MESSAGES.level80;
  if (happiness >= 70) return MESSAGES.level70;
  return MESSAGES.level60;
}

/**
 * 随机获取一条情话（根据好感度）
 * @param {number} happiness - 好感度
 * @param {number} messageCount - 消息计数
 * @returns {string} 情话
 */
export function getLoveMessage(happiness, messageCount) {
  // 每5条消息插入天气情话
  if (messageCount % 5 === 0) {
    const weather = getRandomWeather();
    const weatherMsgs = MESSAGES.weather[weather] || MESSAGES.weather['晴'];
    return weatherMsgs[Math.floor(Math.random() * weatherMsgs.length)];
  }

  const messages = getMessagesByHappiness(happiness);
  return messages[Math.floor(Math.random() * messages.length)];
}

/**
 * 获取互动消息
 * @param {string} type - 互动类型 (click, shake, charge, lowbat)
 * @returns {string} 互动消息
 */
export function getInteractMessage(type) {
  const messages = MESSAGES.interact[type] || MESSAGES.interact.default;
  return messages[Math.floor(Math.random() * messages.length)];
}

/**
 * 随机获取天气
 * @returns {string} 天气类型
 */
function getRandomWeather() {
  const hour = new Date().getHours();
  if (hour >= 19 || hour < 6) return '夜';

  const rand = Math.random();
  if (rand < 0.4) return '晴';
  if (rand < 0.7) return '多云';
  return '雨';
}
